@WebServlet(name = "/CartBuilderServlet", urlPatterns = "/api/addMovieToCart")
public class CartBuilderServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;
    
	// Create a dataSource which is registered in web.xml
//    @Resource(name = "jdbc/moviedb")
//    private DataSource dataSource;
    
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		String movie_id = request.getParameter("movie_id");
		User current = (User)request.getSession().getAttribute("user");
		current.EnterIntoCart(movie_id);
		request.getSession().setAttribute("user", current);
	}

}
